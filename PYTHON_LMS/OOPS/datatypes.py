# Python has several built-in data types, including:

# Integers: whole numbers without decimal points (e.g., 1, 2, 3, -4)

# Floats: numbers with decimal points (e.g., 3.14, -0.5, 1.0)

# Strings: sequences of characters (e.g., "hello", 'world')

# Booleans: either True or False (used for logic and control flow)

# Lists: ordered collections of values (e.g., [1, 2, 3, 4], ["apple", "banana", "cherry"])

# Tuples: ordered, immutable collections of values (e.g., (1, 2, 3), ("hello", "world"))

# Dictionaries: unordered collections of key-value pairs (e.g., {"name": "John", "age": 30, "city": "New York"})

# Sets: unordered collections of unique elements (e.g., {1, 2, 3, 4}, {"apple", "banana", "cherry"})

# In addition to these built-in data types, Python also allows you to define your own custom data types using classes.